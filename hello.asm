; ===== Программа: суммирование массива =====
; Суммирует 5 элементов из памяти начиная с адреса 0x000A

.org  0x0000
.data 0x00FF        ; константа в памяти (резерв)

start:
    LDI R0, #5      ; счётчик итераций
    LDI R1, #0      ; аккумулятор суммы
    LDI R2, #10     ; базовый адрес массива (0x000A)

loop:
    LD  R3, [R2]    ; загрузить элемент массива
    ADD R1, R3      ; R1 += R3
    ADD R2, #1      ; указатель++
    SUB R0, #1      ; счётчик--
    JZ  done        ; если R0==0 — выход из цикла
    JMP loop
