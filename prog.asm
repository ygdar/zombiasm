; ===== Программа: суммирование массива =====
; Суммирует 5 элементов из памяти начиная с адреса 0x000A

.org  0x0000
.data 0x00FF        ; константа в памяти (резерв)

start:
    LDI R0, #5      ; счётчик итераций
    LDI R1, #0      ; аккумулятор суммы
    LDI R2, #10     ; базовый адрес массива (0x000A)

loop:
    LD  R3, [R2]    ; загрузить элемент массива
    ADD R1, R3      ; R1 += R3
    ADD R2, #1      ; указатель++
    SUB R0, #1      ; счётчик--
    JZ  done        ; если R0==0 — выход из цикла
    JMP loop

done:
    AND R1, R1      ; обновить флаги по R1
    JN  error       ; если сумма отрицательная — ошибка
    INT #0          ; yield — передать управление планировщику
    PUSH R1         ; сохранить сумму перед вызовом
    CALL print      ; вызов подпрограммы вывода
    POP  R1         ; восстановить R1
    JMP  finish

error:
    XOR R1, R1      ; обнулить R1 (R1 ^ R1 = 0)
    JMP finish

print:
    POP  R5         ; получить аргумент из стека
    SHL  R5         ; R5 <<= 1  (умножить на 2)
    SHR  R5         ; R5 >>= 1  (вернуть обратно)
    OR   R5, R5     ; обновить флаги без изменения значения
    CMP  R5, R1     ; сравнить R5 и R1 (меняет только флаги)
    JC   overflow   ; если C=1 (перенос) — переполнение
    MOV  R1, R5     ; R1 = R5
    RET             ; возврат из подпрограммы

overflow:
    MOV  R1, R0     ; подставить счётчик как запасное значение
    IRET            ; возврат из обработчика прерывания

finish:
    NOP             ; заглушка / точка для отладчика
    HLT             ; остановка процессора